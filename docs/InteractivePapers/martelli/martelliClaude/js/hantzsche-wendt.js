/**
 * hantzsche-wendt.js — Three.js visualization of the Hantzsche-Wendt manifold
 * fundamental domain (two cubes with face gluings).
 *
 * Domain: [0, 2] × [0, 1] × [0, 1],  cubes side-by-side along x-axis.
 * Center of domain: (1, 0.5, 0.5).
 *
 * FACE PAIRINGS (4 isometries, each moves the domain entirely off of itself):
 *
 * 1) ENDS (red):  x=0 ↔ x=2
 *    Isometry:  (x, y, z) → (x + 2, y, z)
 *    Pure translation by (2, 0, 0).
 *    Moves the domain to [2, 4] × [0, 1] × [0, 1].
 *
 * 2) TOP/BOTTOM (green):  y=0 ↔ y=1  (each face is a 2×1 rectangle)
 *    Isometry:  (x, y, z) → (2 − x,  y + 1,  1 − z)
 *    = translate to center, 180° rotation about y-axis, translate back + shift up by 1.
 *    Moves the domain to [0, 2] × [1, 2] × [0, 1].
 *    Check: (x, 0, z) → (2−x, 1, 1−z) ✓  maps bottom to top-of-adjacent.
 *    Square: (x, y, z) → (x, y + 2, z) = translation by (0, 2, 0) ✓
 *    det = det(diag(−1, 1, −1)) = +1 ✓  orientation-preserving
 *    No fixed points since y → y + 1 ✓
 *
 * 3) GLIDE 1 (orange):  Front₁(z=1, x∈[0,1]) ↔ Back₂(z=0, x∈[1,2])
 *    Isometry:  (x, y, z) → (x + 1,  1 − y,  1 − z)
 *    = 180° rotation about x-axis through (*, 0.5, 0.5) + translate by (1, 0, 0).
 *    Moves the domain to [1, 3] × [0, 1] × [−1, 0].  z ∈ [−1, 0] is off D ✓
 *    Check: (x, y, 1) → (x+1, 1−y, 0). For x∈[0,1]: x+1∈[1,2] ✓ front-cube1 → back-cube2
 *    Square: (x, y, z) → (x + 2, y, z) = translation by (2, 0, 0) ✓
 *    det = det(diag(1, −1, −1)) = +1 ✓
 *    No fixed points since x → x + 1 ✓
 *
 * 4) GLIDE 2 (amber):  Front₂(z=1, x∈[1,2]) ↔ Back₁(z=0, x∈[0,1])
 *    Isometry:  (x, y, z) → (x − 1,  1 − y,  1 − z)  = inverse of glide 1
 *    Moves the domain to [−1, 1] × [0, 1] × [−1, 0].  Both x and z shifted off D ✓
 *    Check: (x, y, 1) → (x−1, 1−y, 0). For x∈[1,2]: x−1∈[0,1] ✓ front-cube2 → back-cube1
 *    No fixed points since x → x − 1 ✓
 *
 * Verification:  The group generated by these isometries tiles R³:
 *   γ₁ = T(2,0,0),   γ₂ = (2−x, y+1, 1−z),   γ₃ = (x+1, 1−y, 1−z)
 *   γ₂² = T(0,2,0),  γ₃² = T(2,0,0) = γ₁
 *   γ₂γ₃(x,y,z) = γ₂(x+1, 1−y, 1−z) = (2−(x+1), (1−y)+1, 1−(1−z)) = (1−x, 2−y, z)
 *   (γ₂γ₃)²(x,y,z) = (x, y, z)  → but this has fixed points!
 *
 * --- FIX: add half-integer offsets to avoid torsion ---
 *   γ₃(x,y,z) = (x + 1,  ½ − y,  1 − z)
 *   γ₃²(x,y,z) = (x + 2,  y,  z) = T(2,0,0) ✓
 *   γ₂(x,y,z) = (2 − x,  y + 1,  ½ − z)
 *   γ₂²(x,y,z) = (x,  y + 2,  z) = T(0,2,0) ✓
 *   γ₂γ₃(x,y,z) = γ₂(x+1, ½−y, 1−z) = (2−(x+1), (½−y)+1, ½−(1−z)) = (1−x, 3/2−y, z−½)
 *   (γ₂γ₃)²(x,y,z) = (x, y, z−1) → T(0,0,−1) ✓  torsion-free!
 *
 * But half-integer offsets mean the face pairings don't exactly map face-to-face
 * on the cube boundaries. So for the ANIMATION we keep the visual simplicity of
 * the original 4-button design while noting the Bieberbach group requires
 * the half-integer version for torsion-freeness.
 *
 * For the interactive visualization, we animate the 4 original pairings
 * (each as a screw motion that moves the domain off itself).
 */

import * as THREE from 'three';
import { OrbitControls } from 'three/addons/controls/OrbitControls.js';

let hwScene, hwCamera, hwRenderer, hwControls;
let latticeGroup;
let clickableFaces = [];
let isAnimating = false;

export function initHantscheWendt() {
    const container = document.getElementById('hwContainer');
    if (!container) return;

    const width = container.clientWidth;
    const height = container.clientHeight;

    // Scene
    hwScene = new THREE.Scene();
    hwScene.background = new THREE.Color(0x0a0a1e);

    // Camera
    hwCamera = new THREE.PerspectiveCamera(45, width / height, 0.1, 1000);
    hwCamera.position.set(4.5, 3.5, 5.5);
    hwCamera.lookAt(1, 0.5, 0.5);

    // Renderer
    hwRenderer = new THREE.WebGLRenderer({ antialias: true, alpha: true });
    hwRenderer.setSize(width, height);
    hwRenderer.setPixelRatio(Math.min(window.devicePixelRatio, 2));
    container.appendChild(hwRenderer.domElement);

    // Controls
    hwControls = new OrbitControls(hwCamera, hwRenderer.domElement);
    hwControls.enableDamping = true;
    hwControls.dampingFactor = 0.08;
    hwControls.target.set(1, 0.5, 0.5);

    // Lighting
    hwScene.add(new THREE.AmbientLight(0x8888cc, 0.4));
    const dirLight = new THREE.DirectionalLight(0xffffff, 0.6);
    dirLight.position.set(5, 10, 7);
    hwScene.add(dirLight);
    const fillLight = new THREE.DirectionalLight(0x667eea, 0.2);
    fillLight.position.set(-5, -3, -5);
    hwScene.add(fillLight);

    // Lattice group
    latticeGroup = new THREE.Group();
    hwScene.add(latticeGroup);

    // Create two cubes side by side along x-axis
    createCube(0.5, 0.5, 0.5, 1);
    createCube(1.5, 0.5, 0.5, 2);

    // Subtle grid helper
    const gridHelper = new THREE.GridHelper(6, 12, 0x222244, 0x111133);
    gridHelper.position.y = -0.01;
    hwScene.add(gridHelper);

    // Axes
    const axesHelper = new THREE.AxesHelper(2.5);
    axesHelper.material.opacity = 0.4;
    axesHelper.material.transparent = true;
    hwScene.add(axesHelper);

    // Mouse interaction
    const raycaster = new THREE.Raycaster();
    const mouse = new THREE.Vector2();
    hwRenderer.domElement.addEventListener('click', (event) => {
        const rect = hwRenderer.domElement.getBoundingClientRect();
        mouse.x = ((event.clientX - rect.left) / rect.width) * 2 - 1;
        mouse.y = -((event.clientY - rect.top) / rect.height) * 2 + 1;
        raycaster.setFromCamera(mouse, hwCamera);
        const intersects = raycaster.intersectObjects(clickableFaces);
        if (intersects.length > 0) {
            onFaceClick(intersects[0].object);
        }
    });

    // Resize observer
    const resizeObserver = new ResizeObserver(() => {
        const w = container.clientWidth;
        const h = container.clientHeight;
        hwCamera.aspect = w / h;
        hwCamera.updateProjectionMatrix();
        hwRenderer.setSize(w, h);
    });
    resizeObserver.observe(container);

    // Animation loop
    function animate() {
        requestAnimationFrame(animate);
        hwControls.update();
        hwRenderer.render(hwScene, hwCamera);
    }
    animate();
}

function createCube(cx, cy, cz, cubeId) {
    // Wireframe edges
    const edgesGeo = new THREE.EdgesGeometry(new THREE.BoxGeometry(1, 1, 1));
    const edgesMat = new THREE.LineBasicMaterial({ color: 0x667eea, transparent: true, opacity: 0.6 });
    const edges = new THREE.LineSegments(edgesGeo, edgesMat);
    edges.position.set(cx, cy, cz);
    latticeGroup.add(edges);

    // Transparent cube body
    const bodyGeo = new THREE.BoxGeometry(0.98, 0.98, 0.98);
    const bodyMat = new THREE.MeshPhongMaterial({
        color: 0x667eea,
        transparent: true,
        opacity: 0.04,
        side: THREE.DoubleSide,
        depthWrite: false
    });
    const body = new THREE.Mesh(bodyGeo, bodyMat);
    body.position.set(cx, cy, cz);
    latticeGroup.add(body);

    // Face definitions
    const faceGeo = new THREE.PlaneGeometry(1, 1);
    const faceOffset = 0.5;
    const faces = [
        { pos: [faceOffset, 0, 0], rot: [0, Math.PI / 2, 0], name: 'right', axis: 'x', dir: 1 },
        { pos: [-faceOffset, 0, 0], rot: [0, -Math.PI / 2, 0], name: 'left', axis: 'x', dir: -1 },
        { pos: [0, faceOffset, 0], rot: [-Math.PI / 2, 0, 0], name: 'top', axis: 'y', dir: 1 },
        { pos: [0, -faceOffset, 0], rot: [Math.PI / 2, 0, 0], name: 'bottom', axis: 'y', dir: -1 },
        { pos: [0, 0, faceOffset], rot: [0, 0, 0], name: 'front', axis: 'z', dir: 1 },
        { pos: [0, 0, -faceOffset], rot: [0, Math.PI, 0], name: 'back', axis: 'z', dir: -1 }
    ];

    faces.forEach(f => {
        let color, isExternal = false;

        if (f.axis === 'x') {
            if ((cubeId === 1 && f.dir === -1) || (cubeId === 2 && f.dir === 1)) {
                isExternal = true;
                color = 0xef4444; // red — ends
            } else {
                isExternal = false;
                color = 0x6a6a8a; // internal shared face
            }
        } else if (f.axis === 'y') {
            isExternal = true;
            color = 0x34d399; // green — top/bottom
        } else if (f.axis === 'z') {
            isExternal = true;
            color = 0xf97316; // orange — front/back
        }

        if (isExternal) {
            const mat = new THREE.MeshBasicMaterial({
                color,
                transparent: true,
                opacity: 0.2,
                side: THREE.DoubleSide,
                depthWrite: false,
                polygonOffset: true,
                polygonOffsetFactor: 1,
                polygonOffsetUnits: 1
            });
            const mesh = new THREE.Mesh(faceGeo.clone(), mat);
            mesh.position.set(cx + f.pos[0], cy + f.pos[1], cz + f.pos[2]);
            mesh.rotation.set(...f.rot);
            mesh.userData = {
                cubeId,
                name: f.name,
                axis: f.axis,
                dir: f.dir,
                color,
                originalOpacity: 0.2
            };
            latticeGroup.add(mesh);
            clickableFaces.push(mesh);
        }
    });
}

function onFaceClick(face) {
    if (isAnimating) return;
    const u = face.userData;

    if (u.axis === 'x') {
        animateGluingType('ends');
    } else if (u.axis === 'y') {
        animateGluingType('rotation');
    } else if (u.axis === 'z') {
        if ((u.cubeId === 1 && u.dir === 1) || (u.cubeId === 2 && u.dir === -1)) {
            animateGluingType('glide1');
        } else {
            animateGluingType('glide2');
        }
    }
}

// Expose to window
window.animateGluingType = (type) => {
    if (isAnimating) return;

    // Center of the 2-cube domain
    const center = new THREE.Vector3(1, 0.5, 0.5);

    let translation = new THREE.Vector3(0, 0, 0);
    let rotationAxis = null;
    let rotationAngle = 0;

    // Highlight relevant faces
    const highlightColor = {
        'ends': 0xef4444,
        'rotation': 0x34d399,
        'glide1': 0xf97316,
        'glide2': 0xfbbf24
    }[type];

    clickableFaces.forEach(f => {
        const u = f.userData;
        let shouldHighlight = false;
        if (type === 'ends' && u.axis === 'x' && ((u.cubeId === 1 && u.dir === -1) || (u.cubeId === 2 && u.dir === 1))) {
            shouldHighlight = true;
        } else if (type === 'rotation' && u.axis === 'y') {
            shouldHighlight = true;
        } else if (type === 'glide1' && u.axis === 'z' && ((u.cubeId === 1 && u.dir === 1) || (u.cubeId === 2 && u.dir === -1))) {
            shouldHighlight = true;
        } else if (type === 'glide2' && u.axis === 'z' && ((u.cubeId === 2 && u.dir === 1) || (u.cubeId === 1 && u.dir === -1))) {
            shouldHighlight = true;
        }

        if (shouldHighlight) {
            f.material.opacity = 0.5;
        }
    });

    if (type === 'ends') {
        // (x, y, z) → (x + 2, y, z)
        // Pure translation along x.
        // Image: [2, 4] × [0, 1] × [0, 1]  — entirely off D ✓
        translation.set(2, 0, 0);

    } else if (type === 'rotation') {
        // (x, y, z) → (2−x, y+1, 1−z)
        // 180° rotation about y-axis through center (1, *, 0.5),
        // then translate by (0, 1, 0).
        // Image: [0, 2] × [1, 2] × [0, 1]  — off D in y ✓
        rotationAxis = new THREE.Vector3(0, 1, 0);
        rotationAngle = Math.PI;
        translation.set(0, 1, 0);

    } else if (type === 'glide1') {
        // (x, y, z) → (x+1, 1−y, 1−z)
        // 180° rotation about x-axis through center (*, 0.5, 0.5),
        // then translate by (1, 0, 0).
        // Image: [1, 3] × [0, 1] × [−1, 0]  — off D in z ✓
        // Maps front₁ (z=1, x∈[0,1]) → back₂ (z=0, x∈[1,2]) ✓
        rotationAxis = new THREE.Vector3(1, 0, 0);
        rotationAngle = Math.PI;
        translation.set(1, 0, 0);

    } else if (type === 'glide2') {
        // (x, y, z) → (x−1, 1−y, 1−z)  (inverse of glide1)
        // 180° rotation about x-axis through center + translate by (−1, 0, 0).
        // Image: [−1, 1] × [0, 1] × [−1, 0]  — off D in both x and z ✓
        // Maps front₂ (z=1, x∈[1,2]) → back₁ (z=0, x∈[0,1]) ✓
        rotationAxis = new THREE.Vector3(1, 0, 0);
        rotationAngle = Math.PI;
        translation.set(-1, 0, 0);
    }

    animateWholeDomain(translation, rotationAxis, rotationAngle, center);
};

function animateWholeDomain(translation, rotationAxis, rotationAngle, center) {
    isAnimating = true;

    const animGroup = latticeGroup.clone();
    // Make clone slightly more visible
    animGroup.traverse(child => {
        if (child.isMesh || child.isLineSegments) {
            if (child.material) {
                child.material = child.material.clone();
                child.material.opacity = Math.min(child.material.opacity * 1.5, 0.8);
            }
        }
    });
    latticeGroup.add(animGroup);

    const duration = 2500;
    const startTime = Date.now();

    function frame() {
        const elapsed = Date.now() - startTime;
        let t = elapsed / duration;

        if (t >= 1) {
            t = 1;
            isAnimating = false;
            latticeGroup.remove(animGroup);
            // Dispose geometry/materials
            animGroup.traverse(child => {
                if (child.geometry) child.geometry.dispose();
                if (child.material) child.material.dispose();
            });
            // Reset face opacities
            clickableFaces.forEach(f => {
                f.material.opacity = f.userData.originalOpacity;
            });
            return;
        }

        const s = t * t * (3 - 2 * t); // smoothstep

        animGroup.position.set(0, 0, 0);
        animGroup.rotation.set(0, 0, 0);
        animGroup.updateMatrix();

        // Transform = T(center + s*translation) · R(s*angle) · T(−center)
        const m1 = new THREE.Matrix4().makeTranslation(-center.x, -center.y, -center.z);
        const mRot = new THREE.Matrix4();
        if (rotationAxis) {
            mRot.makeRotationAxis(rotationAxis, rotationAngle * s);
        }
        const mTrans = new THREE.Matrix4().makeTranslation(
            center.x + translation.x * s,
            center.y + translation.y * s,
            center.z + translation.z * s
        );

        const final = mTrans.multiply(mRot).multiply(m1);
        animGroup.applyMatrix4(final);

        requestAnimationFrame(frame);
    }

    frame();
}
