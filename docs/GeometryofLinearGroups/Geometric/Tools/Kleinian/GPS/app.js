function setupUI() {
    const $ = id => document.getElementById(id);

    // Populate preset select
    const sel = $('presetSelect');
    sel.innerHTML = '';
    EXAMPLES.forEach((ex, i) => {
        const opt = document.createElement('option');
        opt.value = i; opt.textContent = ex.name;
        sel.appendChild(opt);
    });
    sel.addEventListener('change', e => { curExample = parseInt(e.target.value); rebuildScene(); });

    // Sliders
    const bindSlider = (id, valId, fn) => {
        $(id).addEventListener('input', () => {
            $(valId).textContent = parseFloat($(id).value).toFixed(2);
            fn(parseFloat($(id).value));
            rebuildScene();
        });
    };
    bindSlider('separationSlider', 'separationValue', v => separation = v);
    bindSlider('opacitySlider', 'opacityValue', v => opacity = v);

    // Toggles
    $('showHypersurface').addEventListener('change', e => { showCut = e.target.checked; rebuildScene(); });
    $('showWireframe').addEventListener('change', e => { showWireframe = e.target.checked; rebuildScene(); });
    $('autoRotate').addEventListener('change', e => { autoRotate = e.target.checked; controls.autoRotate = autoRotate; });
    $('colorScheme').addEventListener('change', e => { curPalette = parseInt(e.target.value); rebuildScene(); });

    // Keyboard
    addEventListener('keydown', e => {
        if (e.key === 'h' || e.key === 'H') $('controls').classList.toggle('hidden');
        if (e.key === 'i' || e.key === 'I') $('infoPanel').classList.toggle('collapsed');
    });

    // Screenshot
    $('screenshotBtn').addEventListener('click', () => {
        renderer.render(scene, camera);
        const a = document.createElement('a');
        a.download = 'gps-manifold.png';
        a.href = renderer.domElement.toDataURL('image/png');
        a.click();
    });

    $('toggleInfo').addEventListener('click', () => $('infoPanel').classList.toggle('collapsed'));

    setTimeout(() => { const inst = $('instructions'); if (inst) inst.style.opacity = '0'; }, 6000);
}

